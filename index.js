const KeyboardModuleSolver = (() => {
  console.log("KeyboardData init");
  // Keyboard data
  const keyboardData = KeyboardData;
  let keyboardContainer = null;
  const tabsData = [
      { id: 'abc', value: 'ABC' },
      { id: 'algebra', value: 'Algebra' },
      { id: 'calculus', value: 'Calculus' },
      { id: 'variables', value: 'Variables' },
      { id: 'trigonometry', value: 'Trigonometry' },
      { id: 'statistics', value: 'Statistics' },
      { id: 'matrices', value: 'Matrices' },
      { id: 'landg', value: 'Latin and Greek' },
  ];

  const generateKeyboard = () => {
      // Create custom keyboard
      console.log('Create custom keyboard');
      keyboardContainer = document.getElementById('custom-keyboard');
      console.log(keyboardContainer);
      // Create custom keyboard

      const mfe = new MathfieldElement();
      console.log(document.getElementById('math-field'))
      mfe.addEventListener("focus", (e) => {
          if (
              event.target !== mathField?.$el() && // Check if the target is not the math field
              !keyboardContainer.contains(event.target) // Check if the target is not a descendant of the keyboard
          ) {
              keyboardContainer.style.display = 'block'; // Hide the keyboard
          } else {
              keyboardContainer.style.display = "none"
          }
      });


      const mathField = mfe.mathField;
      document.getElementById('math-field').appendChild(mfe);


      // Clear previous keyboard
      keyboardContainer.innerHTML = '';

      // Create tab navigation
      const keyboardNavigation = document.createElement('div');
      keyboardNavigation.className = 'keyboard-navigation';
      tabsData.forEach(tab => {
          const tabButton = document.createElement('button');
          tabButton.className = 'keyboard-nav-tab';
          tabButton.innerText = tab.value;
          tabButton.dataset.tab = `tab-${tab.id}`;
          keyboardNavigation.appendChild(tabButton);
      });

      // tab slider
      let previous = document.createElement("button");
      let tabs = document.createElement("div");
      let next = document.createElement("button");
      previous.innerText = "<";
      next.innerText = ">";
      previous.className = "scroll-left";
      tabs.className = "tabs";
      next.className = "scroll-right";

      tabs.appendChild(previous);
      tabs.appendChild(keyboardNavigation);
      tabs.appendChild(next);
      keyboardContainer.appendChild(tabs);

      // Create tab content
      tabsData.forEach(tab => {
          const tabContent = document.createElement('div');
          tabContent.className = 'keyboard-tab';
          tabContent.id = `tab-${tab.id}`;

          const tabData = keyboardData[tab.id];

          tabData.forEach(keyData => {
              const keyElement = document.createElement("math-field");
              keyElement.readOnly = true;
              keyElement.innerText = keyData.key;
              keyElement.className = "inner-math-field";

              const button = document.createElement("button");
              button.className = keyData.class + " math-field-button";

              button.addEventListener('mousedown', event => {
                  event.preventDefault(); // Prevent losing focus on click
                  mfe.executeCommand(['insert', keyData.value]);
                  mfe.focus();
              });

              if (keyData.img) {
                  button.style.backgroundImage = "url('./assets/images/keys/" + keyData.img + "')";
              } else {
                  button.innerText = keyData.key;
                  // keyElement.innerText = keyData.key;
                  // button.appendChild(keyElement);
              }

              button.dataset.value = keyData.value;
              button.dataset.command = keyData.command || "";
              tabContent.appendChild(button);
          });

          keyboardContainer.appendChild(tabContent);
      });
      // Set initial active tab
      const initialTabId = tabsData[0].id;
      switchTab(initialTabId);
      // Rest of the code...
  };

  const switchTab = tabId => {
      const activeTab = document.getElementById(`tab-${tabId}`);
      if (!activeTab) return;

      const allTabs = document.querySelectorAll('.keyboard-tab');
      const allNavTabs = document.querySelectorAll('.keyboard-nav-tab');

      allTabs.forEach(tab => tab.classList.remove('active'));
      activeTab.classList.add('active');

      allNavTabs.forEach(navTab => {
          if (navTab.dataset.tab === `tab-${tabId}`) {
              navTab.classList.add('active');
          } else {
              navTab.classList.remove('active');
          }
      });
  };

  const handleKeyboardClick = event => {
      if (event.target && event.target.classList.contains("scroll-right")) {
          console.log("scroll-right");
          let scrollLeft =
              event.target.parentNode.firstChild.nextSibling.scrollLeft + 100;
          event.target.parentNode.firstChild.nextSibling.scrollTo(scrollLeft, 0);
          return;
      }
      if (
          event.target &&
          event.target.classList.contains("scroll-left")
      ) {
          console.log("scroll-left");
          let scrollLeft =
              event.target.parentNode.firstChild.nextSibling.scrollLeft - 100;

          console.log(event.target.parentNode.firstChild.nextSibling, scrollLeft);

          event.target.parentNode.firstChild.nextSibling.scrollTo(scrollLeft, 0);
          return;
      }
      const target = event.target;
      if (target.classList.contains('keyboard-nav-tab')) {
          const tabId = target.dataset.tab.replace('tab-', '');
          switchTab(tabId);
      }
  };

  const handleOutsideClick = event => {
      const mathField = document.getElementById('math-field').mathField;

      // Code for handling clicks outside the keyboard and math field...
      if (
          event.target !== mathField?.$el() && // Check if the target is not the math field
          !keyboardContainer.contains(event.target) // Check if the target is not a descendant of the keyboard
      ) {
          keyboardContainer.style.display = 'none'; // Hide the keyboard
      }
  };
  return {
      generateKeyboard,
      switchTab,
      handleKeyboardClick,
      handleOutsideClick,
  };
})();

